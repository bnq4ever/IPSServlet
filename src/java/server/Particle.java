public class Particle {
    public double x;
    public double y;
    public double direction;
    public double speed;
    public double weight;

    public Particle(double x, double y, double direction, double speed, double weight) {
        this.x = x;
        this.x = y;
        this.x = direction;
        this.x = speed;
        this.x = weight;
    }

}